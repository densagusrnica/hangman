from turtle import *

def draw_window(width, height, X = True):
    pendown()
    setheading(0)
    forward(width//2)
    setheading(90)
    forward(height)
    setheading(180)
    forward(width)
    setheading(270)
    forward(height)
    setheading(0)
    if X:
        forward(width//2)
        setheading(90)
        forward(height)
        back(height//2)
        setheading(0)
        forward(width//2)
        back(width)
        forward(width//2)
        setheading(270)
        forward(height//2)
        setheading(0)
    penup()

